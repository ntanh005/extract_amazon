package com.ntanh.extract;

import java.util.Date;

public class AmazonAsin {
	String asin_code;
	String dt_asin_code;
	int type;
	Date date;
	public String getAsin_code() {
		return asin_code;
	}
	public void setAsin_code(String asin_code) {
		this.asin_code = asin_code;
	}
	public String getDt_asin_code() {
		return dt_asin_code;
	}
	public void setDt_asin_code(String dt_asin_code) {
		this.dt_asin_code = dt_asin_code;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	
}
